void initScreen(codal::STM32STEAM32_WB55RG& steam32, STM32SPI& spi, STM32Pin& cs, STM32Pin& dc)
{
    spi.beginTransaction();
    dc.setDigitalValue(0);
    cs.setDigitalValue(0);

    spi.writeTransaction(0xae);  //--turn off oled panel

    spi.writeTransaction(0x15);  //   set column address
    spi.writeTransaction(0x00);  //  start column   0
    spi.writeTransaction(0x7f);  //  end column   127

    spi.writeTransaction(0x75);  //   set row address
    spi.writeTransaction(0x00);  //  start row   0
    spi.writeTransaction(0x7f);  //  end row   127

    spi.writeTransaction(0x81);  // set contrast control
    spi.writeTransaction(0x80);

    spi.writeTransaction(0xa0);  // gment remap
    spi.writeTransaction(0x51);  // 51

    spi.writeTransaction(0xa1);  // start line
    spi.writeTransaction(0x00);

    spi.writeTransaction(0xa2);  // display offset
    spi.writeTransaction(0x00);

    spi.writeTransaction(0xa4);  // rmal display
    spi.writeTransaction(0xa8);  // set multiplex ratio
    spi.writeTransaction(0x7f);

    spi.writeTransaction(0xb1);  // set phase leghth
    spi.writeTransaction(0xf1);

    spi.writeTransaction(0xb3);  // set dclk
    spi.writeTransaction(0x00);  // 80Hz:0xc1 90Hz:0xe1   100Hz:0x00   110Hz:0x30 120Hz:0x50   130Hz:0x70     01

    spi.writeTransaction(0xab);  //
    spi.writeTransaction(0x01);  //

    spi.writeTransaction(0xb6);  // set phase leghth
    spi.writeTransaction(0x0f);

    spi.writeTransaction(0xbe);
    spi.writeTransaction(0x0f);

    spi.writeTransaction(0xbc);
    spi.writeTransaction(0x08);

    spi.writeTransaction(0xd5);
    spi.writeTransaction(0x62);

    spi.writeTransaction(0xfd);
    spi.writeTransaction(0x12);

    spi.writeTransaction(0xAF);
    spi.endTransaction();

    cs.setDigitalValue(1);
}

void showPic(STM32SPI& spi, STM32Pin& cs, STM32Pin& dc)
{
    cs.setDigitalValue(0);

    spi.beginTransaction();
    dc.setDigitalValue(0);

    spi.writeTransaction(0x15);  // Set Column Address
    spi.writeTransaction(0x00);  // Start Column Address
    spi.writeTransaction(0x3F);  // End Column Address

    spi.writeTransaction(0x75);  // Set Row Address (segment)
    spi.writeTransaction(0x00);  // Satrt Row Address
    spi.writeTransaction(0x7F);  // End Row Address

    spi.endTransaction();

    spi.beginTransaction();
    dc.setDigitalValue(1);

    for (int i = 0; i < 128; i++) {
        for (int j = 0; j < 128 / 2; j++) {
            spi.writeTransaction(pic[i][j]);
        }
    }

    spi.endTransaction();

    cs.setDigitalValue(1);
    dc.setDigitalValue(0);
}
